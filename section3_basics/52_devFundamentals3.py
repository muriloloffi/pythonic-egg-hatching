# Understanding Data Structures: An experienced programmer knows when to use a data
# structure over another, what the tradeoffs are. This takes practice. When to use each?
# Lists are good to keep data in order, and to access it by index. Dictionaries are good
# to access data by key, but they don't keep the order of the data. Also, dictionaries
# hold more information than lists. That is, a list is a sequence of elements, while a
# dictionary is a sequence of key-value pairs. Later on, we'll see tuples and sets,
# which are also data structures. We'll see when to use each one of them.
