# Break, continue and pass keywords

# Break keyword stops the execution of the loop, be it for or while

# Continue tells the interpreter that in a singular iteration of the loop, every
# statement after the continue should be ignored and it'll continue from the
# very next element of the data structure that is being iterated over.

# Finally, pass is just a placeholder, a way to make a loop exist without
# requiring us to have any logic placed and without throwing any errors
